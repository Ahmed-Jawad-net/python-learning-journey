x="Python "
y="is "
z="awesome"

print(x+y+z) # it will print "Python is awesome" because the + operator is used to concatenate (join) the strings together. The variables x, y, and z are added together to create a single string that is printed to the console.


x = 5
y = "John"
print(x, y) # it will print "5 John" because the print() function can take multiple arguments, and it will print them separated by a space by default. In this case, x is an integer and y is a string, but they can still be printed together without any issues.
